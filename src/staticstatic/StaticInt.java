package staticstatic;

public class StaticInt {
	
	   public static void number() {
		   int num = 5;
		   int num1 = 10;
		   int sum = num+num1;
		   System.out.println(sum);
	   }
	
	   public static void number1() {
		   int num = 50;
		   int num1 = 10;
		   int sum = num+num1;
		   System.out.println(sum);
	   }
	

	public static void main(String[] args) {
		
		StaticInt.number();
		StaticInt.number1();
	}

}
