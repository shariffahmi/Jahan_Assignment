package staticstatic;

public class StaticDouble {
	
	public static void number() {
		   double num = 5.5;
		   double num1 = 10;
		   double sum = num*num1;
		   System.out.println(sum);
	   }
	
	   public static void number1() {
		   double num = 5.1;
		   double num1 = 10.2;
		   double sum = num+num1;
		   System.out.println(sum);
	
	
	   }
	

	public static void main(String[] args) {
		StaticDouble.number();
		StaticDouble.number1();
		

	}

}
